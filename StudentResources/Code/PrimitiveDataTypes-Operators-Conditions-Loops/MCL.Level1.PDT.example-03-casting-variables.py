# Example 3: Casting variables to specify data types
m = str(10)       # m will be the string '10'
n = int("42")     # n will be the integer 42 (converted from string)
o = float("2.718") # o will be the floating point number 2.718
print("Example 3:")
print("m:", m, type(m))
print("n:", n, type(n))
print("o:", o, type(o))
