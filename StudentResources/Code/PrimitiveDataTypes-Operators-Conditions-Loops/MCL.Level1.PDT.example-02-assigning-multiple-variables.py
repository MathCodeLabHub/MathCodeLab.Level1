# Example 2: Assigning multiple variables in one line
a, b, c = 7, 3.14, "apples"   # a is int, b is float, c is string
print("Example 2:")
print("a =", a, "b =", b, "c =", c)

# You can also assign the same value to multiple variables in one line.
p = q = r = "common"
print("p =", p, "q =", q, "r =", r)
